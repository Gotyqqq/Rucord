// Часто используемые и полный список эмодзи (как в Discord)

const RECENT_KEY = 'rucord_recent_emojis';
const MAX_RECENT = 24;

export function getRecentEmojis() {
  try {
    const raw = localStorage.getItem(RECENT_KEY);
    if (!raw) return [];
    const list = JSON.parse(raw);
    return Array.isArray(list) ? list.slice(0, MAX_RECENT) : [];
  } catch {
    return [];
  }
}

export function addRecentEmoji(emoji) {
  try {
    let list = getRecentEmojis();
    list = [emoji, ...list.filter((e) => e !== emoji)].slice(0, MAX_RECENT);
    localStorage.setItem(RECENT_KEY, JSON.stringify(list));
  } catch {}
}

